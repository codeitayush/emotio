const ThemeContext = createContext();

export const useTheme = () => useContext(ThemeContext);

export const ThemeProvider = ({ children }) => {
  const [currentTheme, setCurrentTheme] = useState('default');

  useEffect(() => {
    // Add CSS transition to document root on first load
    const root = document.documentElement;
    root.style.setProperty(
      'transition',
      'background-color 0.4s ease, color 0.4s ease, border-color 0.4s ease, fill 0.4s ease, stroke 0.4s ease'
    );

    // Apply theme CSS variables to document root
    const themeColors = themes[currentTheme] || themes.default;

    // Primary colors
    Object.entries(themeColors.primary).forEach(([shade, color]) => {
      root.style.setProperty(`--color-primary-${shade}`, color);
    });

    // Secondary colors
    Object.entries(themeColors.secondary).forEach(([shade, color]) => {
      root.style.setProperty(`--color-secondary-${shade}`, color);
    });

    // Accent colors
    Object.entries(themeColors.accent).forEach(([shade, color]) => {
      root.style.setProperty(`--color-accent-${shade}`, color);
    });
  }, [currentTheme]);

  const value = {
    currentTheme,
    setTheme: (theme) => setCurrentTheme(themes[theme] ? theme : 'default'),
    availableThemes: Object.keys(themes),
  };

  return (
    <ThemeContext.Provider value={value}>{children}</ThemeContext.Provider>
  );
};

export default ThemeProvider;
